// Package that handles key pair and encryption/decryption methods
package encryption

import (
	"bytes"
	"crypto/rand"
	"crypto/rsa"
	"crypto/sha256"
	"crypto/x509"
	"encoding/pem"
	"errors"
	"os"
)

func GeneratePairKey(bitSize int) (*rsa.PrivateKey, error) {
	reader := rand.Reader
	return rsa.GenerateKey(reader, bitSize)
}

func ExportPrivateKey(privateKey *rsa.PrivateKey) []byte {
	privateKeyBytes := x509.MarshalPKCS1PrivateKey(privateKey)
	privateKeyPEM := pem.EncodeToMemory(
		&pem.Block{
			Type:  "RSA PRIVATE KEY",
			Bytes: privateKeyBytes,
		},
	)
	return privateKeyPEM
}

func ParsePrivateKeyFile(path string) (privateKey *rsa.PrivateKey, err error) {
	var databaseKeyReader *os.File
	if databaseKeyReader, err = os.Open(path); err != nil {
		panic(err)
	}
	defer databaseKeyReader.Close()
	privateKeyBuffer := &bytes.Buffer{}
	if _, err = privateKeyBuffer.ReadFrom(databaseKeyReader); err != nil {
		panic(err)
	}
	if privateKey, err = ParsePrivateKey(privateKeyBuffer.Bytes()); err != nil {
		panic(err)
	}
	return
}

func ParsePrivateKey(privatePEM []byte) (*rsa.PrivateKey, error) {
	block, _ := pem.Decode([]byte(privatePEM))
	if block == nil {
		return nil, errors.New("failed to parse PEM block containing the key")
	}

	privateKey, err := x509.ParsePKCS1PrivateKey(block.Bytes)
	if err != nil {
		return nil, err
	}

	return privateKey, nil
}

func ExportPublicKey(publicKey *rsa.PublicKey) ([]byte, error) {
	publicKeyBytes, err := x509.MarshalPKIXPublicKey(publicKey)
	if err != nil {
		return nil, err
	}
	publicKeyPEM := pem.EncodeToMemory(
		&pem.Block{
			Type:  "RSA PUBLIC KEY",
			Bytes: publicKeyBytes,
		},
	)

	return publicKeyPEM, nil
}

func ParsePublicKey(pubPEM []byte) (*rsa.PublicKey, error) {
	block, _ := pem.Decode([]byte(pubPEM))
	if block == nil {
		return nil, errors.New("failed to parse PEM block containing the key")
	}

	publicKey, err := x509.ParsePKIXPublicKey(block.Bytes)
	if err != nil {
		return nil, err
	}

	switch publicKeyType := publicKey.(type) {
	case *rsa.PublicKey:
		return publicKeyType, nil
	default:
		break
	}
	return nil, errors.New("key type is not RSA")
}

func Encrypt(public *rsa.PublicKey, msg []byte) ([]byte, error) {
	hash := sha256.New()
	random := rand.Reader
	msgLen := len(msg)
	step := public.Size() - 2*hash.Size() - 2
	var encryptedBytes []byte

	for start := 0; start < msgLen; start += step {
		finish := start + step
		if finish > msgLen {
			finish = msgLen
		}

		encryptedBlockBytes, err := rsa.EncryptOAEP(hash, random, public, msg[start:finish], nil)
		if err != nil {
			return nil, err
		}

		encryptedBytes = append(encryptedBytes, encryptedBlockBytes...)
	}

	return encryptedBytes, nil
}

func Decrypt(private *rsa.PrivateKey, msg []byte) ([]byte, error) {
	hash := sha256.New()
	random := rand.Reader
	msgLen := len(msg)
	step := private.PublicKey.Size()
	var decryptedBytes []byte

	for start := 0; start < msgLen; start += step {
		finish := start + step
		if finish > msgLen {
			finish = msgLen
		}

		decryptedBlockBytes, err := rsa.DecryptOAEP(hash, random, private, msg[start:finish], nil)
		if err != nil {
			return nil, err
		}

		decryptedBytes = append(decryptedBytes, decryptedBlockBytes...)
	}

	return decryptedBytes, nil
}
